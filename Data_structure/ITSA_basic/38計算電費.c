#include <stdio.h>
int main() {
    int num;
    scanf("%d", &num);

    float s, ns;
    if (num <= 120) {
        s = 2.10 * num;
        ns = 2.10 * num;
    } else if (num <= 330) {
        s = 2.10 * 120 + (num - 120) * 3.02;
        ns = 2.10 * 120 + (num - 120) * 2.68;
    } else if (num <= 500) {
        s = 2.10 * 120 + (330 - 120) * 3.02 + (num - 330) * 4.39;
        ns = 2.10 * 120 + (330 - 120) * 2.68 + (num - 330) * 3.61;
    } else if (num <= 700) {
        s = 2.10 * 120 + (330 - 120) * 3.02 + (500 - 330) * 4.39 + (num - 500) * 4.97;
        ns = 2.10 * 120 + (330 - 120) * 2.68 + (500 - 330) * 3.61 + (num - 500) * 4.01;
    } else {
        s = 2.10 * 120 + (330 - 120) * 3.02 + (500 - 330) * 4.39 + (700 - 500) * 4.97 + (num - 700) * 5.63;
        ns = 2.10 * 120 + (330 - 120) * 2.68 + (500 - 330) * 3.61 + (700 - 500) * 4.01 + (num - 700) * 4.50;
    }

    printf("Summer months:%.2f\n", s);
    printf("Non-Summer months:%.2f\n", ns);
}
